    public partial class RoundedTb : UserControl
    {
        private Color borderColor = Color.MediumSlateBlue;
        private Color borderFocusColor = Color.HotPink;
        private int borderSize = 1;
        private bool underlinedStyle = false;
        private bool isFocused = false;

        private int borderRadius = 8;

        private void rtb_content_ContentsResized(object sender, ContentsResizedEventArgs e)
        {
            RichTextBox rtb = (RichTextBox)sender;
            int txtWidth = TextRenderer.MeasureText(rtb.Text, this.Font).Width + 1;
            int txtHeight = TextRenderer.MeasureText(rtb.Text, this.Font).Height + 1;
            rtb.Size = new Size(this.Width - 20, (txtWidth / (this.Width - 20)) * txtHeight + txtHeight);
            this.Height = rtb.Height + this.Padding.Top + this.Padding.Bottom;
        }
        public RoundedTb()
        {
            InitializeComponent();
        }

        public void Select(int start, int length)
        {
            textBox1.Select(start, length);
        }

        #region -> Private methods

        private GraphicsPath GetFigurePath(Rectangle rect, int radius)
        {
            GraphicsPath path = new GraphicsPath();
            float curveSize = radius * 2F;

            path.StartFigure();
            path.AddArc(rect.X, rect.Y, curveSize, curveSize, 180, 90);
            path.AddArc(rect.Right - curveSize, rect.Y, curveSize, curveSize, 270, 90);
            path.AddArc(rect.Right - curveSize, rect.Bottom - curveSize, curveSize, curveSize, 0, 90);
            path.AddArc(rect.X, rect.Bottom - curveSize, curveSize, curveSize, 90, 90);
            path.CloseFigure();
            return path;
        }
        private void SetTextBoxRoundedRegion()
        {
            GraphicsPath pathTxt;
            if (Multiline)
            {
                pathTxt = GetFigurePath(textBox1.ClientRectangle, borderRadius - borderSize);
                textBox1.Region = new Region(pathTxt);
            }
            else
            {
                pathTxt = GetFigurePath(textBox1.ClientRectangle, borderSize * 2);
                textBox1.Region = new Region(pathTxt);
            }
            pathTxt.Dispose();
        }
        private void UpdateControlHeight()
        {
            int txtHeight = TextRenderer.MeasureText("Text", this.Font).Height + 1;
            textBox1.MinimumSize = new Size(0, txtHeight);

            this.Height = textBox1.Height + this.Padding.Top + this.Padding.Bottom;
        }
        #endregion

